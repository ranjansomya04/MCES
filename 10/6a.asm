	AREA RESET,CODE
		ENTRY
		LDR R0,=INPUT
		BL FACT
		LDR R1,=0x40000000
		STR R3,[R1]
STOP	B STOP

FACT
		LDR R2,[R0]
		CMP R2,#00
		BEQ END1
		MOV R3,R2
		
LOOP
		SUB R2,#01
		CMP R2,#00
		MULNE R3,R2,R3
		BNE LOOP
		MOV PC,LR
		
END1
		MOV R3,#01
END2
		MOV PC,LR
		
INPUT DCD &A
	
	END